#ifndef LLVM_LIB_TARGET_NEMESYS_INSTPRINTER_NEMESYSINSTPRINTER_H
#define LLVM_LIB_TARGET_NEMESYS_INSTPRINTER_NEMESYSINSTPRINTER_H

#include "llvm/ADT/StringRef.h"
#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class MCInst;

class NemesysInstPrinter : public MCInstPrinter {
public:
  NemesysInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                     const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printInst(const MCInst *MI, raw_ostream &O, StringRef Annot,
                 const MCSubtargetInfo &STI) override;
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O,
                    const char *Modifier = nullptr);
  void printCondCode(const MCInst *MI, unsigned OpNo, raw_ostream &O) const;
  void printNegatablePred(const MCInst *MI, unsigned OpNo, raw_ostream &O) const;

  // Autogenerated by tblgen.
  void printInstruction(const MCInst *MI, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);
};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_NEMESYS_INSTPRINTER_NEMESYSINSTPRINTER_H
